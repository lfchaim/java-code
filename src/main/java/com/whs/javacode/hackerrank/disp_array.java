package com.whs.javacode.hackerrank;

public class disp_array
{

	public static void main(String[] args)
	{
		
		int [] data = new int [10];
		
		
		
	}
	
	public static void disp(int [] data)
	{
		for(int i =0;i<data.length;i++)
		{
			System.out.print(data[i]+" ");
		}
		System.out.println("");
	}

}
