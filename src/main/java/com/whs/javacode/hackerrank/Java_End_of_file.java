package com.whs.javacode.hackerrank;

import java.util.Scanner;

public class Java_End_of_file
{

	public static void main(String[] args)
	{
		
		Scanner s=new Scanner(System.in);
		int tem = 1;
		while(s.hasNext())
		{
			System.out.println(tem +" "+s.nextLine());
			tem++;
		}
		
		
		
		
	
		
	}

}
